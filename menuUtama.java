    private void updateComboBoxFromTable() {
        // Bersihkan ComboBox sebelum menambahkan data baru
        cb_keterangan.removeAllItems();

        // Ambil model dari tabel
        DefaultTableModel tableModel = (DefaultTableModel) tabel_mc.getModel();

        // Iterasi setiap baris di tabel
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            // Ambil nilai dari kolom pertama
            String item = tableModel.getValueAt(i, 0).toString();

            // Tambahkan ke ComboBox
            cb_keterangan.addItem(item);
        }
        cb_keterangan.addItem("Dana Darurat");
    }

    

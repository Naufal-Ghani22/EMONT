    private void btn_deleterecapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleterecapActionPerformed
        // TODO add your handling code here:
        try {
            // Ambil model tabel
            DefaultTableModel tabelRecap = (DefaultTableModel) tabel_recap.getModel();

            // Cek apakah ada baris yang dipilih
            int selectedRow = tabel_recap.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Silakan pilih baris yang ingin dihapus.");
                return;
            }

            // Konfirmasi penghapusan
            int confirm = JOptionPane.showConfirmDialog(null, 
                "Apakah Anda yakin ingin menghapus data ini?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Hapus baris yang dipilih
                tabelRecap.removeRow(selectedRow);

                // Perbarui nomor baris
                for (int i = 0; i < tabelRecap.getRowCount(); i++) {
                    tabelRecap.setValueAt(i + 1, i, 0); // Kolom No (index 0)
                }

                // Perbarui saldo
                double saldo = Double.parseDouble(txt_uangbulanan.getText());
                for (int i = 0; i < tabelRecap.getRowCount(); i++) {
                    double pemasukan = tabelRecap.getValueAt(i, 1).equals("-") ? 0 
                            : Double.parseDouble(tabelRecap.getValueAt(i, 1).toString());
                    double pengeluaran = tabelRecap.getValueAt(i, 2).equals("-") ? 0 
                            : Double.parseDouble(tabelRecap.getValueAt(i, 2).toString());
                    saldo += pemasukan - pengeluaran;

                    // Update saldo pada tabel
                    tabelRecap.setValueAt(saldo, i, 3);
                }

                // Tampilkan saldo terbaru di field_saldorecap
                field_saldorecap.setText("Rp. " + String.format("%.2f", saldo));

                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_deleterecapActionPerformed

        
    
    
    
